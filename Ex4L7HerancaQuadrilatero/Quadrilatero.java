package Ex4L7HerancaQuadrilatero;

public class Quadrilatero {
    private double x1, x2, y1, y2,x3, y3, x4, y4;

    Quadrilatero (double x1, double y1, double x2, double y2, double x3, double y3, double x4, double y4){
        this.x1=x1;
        this.y1=y1;
        this.x2=x2;
        this.y2=y2;
        this.x3=x3;
        this.y3=y3;
        this.x4=x4;
        this.y4=y4;
    }

    public double getX1(){
        return x1;
    }
    public double getX2(){
        return x2;
    }
    public double getX3(){
        return x3;
    }
    public double getX4(){
        return x4;
    }
    public double getY1(){
        return y1;
    }
    public double getY2(){
        return y2;
    }
    public double getY3(){
        return y3;
    }
    public double getY4(){
        return y4;
    }
    
}
